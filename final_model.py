class GaussianClassifier(nn.Module):

    def __init__(self,
                 in_features=1280,
                 hidden_dim=32,
                 num_classes=2):

        super().__init__()

        self.mu1 = nn.Parameter(
            torch.zeros(in_features)
        )

        self.sigma1 = nn.Parameter(
            torch.ones(in_features)
        )

        self.fc1 = nn.Linear(
            in_features,
            hidden_dim
        )

        self.relu = nn.ReLU()

        self.fc2 = nn.Linear(
            hidden_dim,
            num_classes
        )

        self.mu2 = nn.Parameter(
            torch.zeros(num_classes)
        )

        self.sigma2 = nn.Parameter(
            torch.ones(num_classes)
        )

    def forward(self, x, print_z=False):

        sigma1 = torch.abs(self.sigma1) + 1e-6

        z1 = (x - self.mu1) / sigma1

        z = self.fc1(z1)

        z = self.relu(z)

        z = self.fc2(z)

        sigma2 = torch.abs(self.sigma2) + 1e-6

        z2 = (z - self.mu2) / sigma2

        if print_z:

            print("\n===== Z1 Summary =====")
            print("Mean :", z1.mean().item())
            print("Std  :", z1.std().item())

            print("\n===== Z2 =====")
            print(z2.detach().cpu().numpy())

        return z2

