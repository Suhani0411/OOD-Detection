def get_confidence(model, image_tensor):

    model.eval()

    with torch.no_grad():

        z2 = model(
            image_tensor,
            print_z=True
        )

        print("\nRaw Z2:")
        print(z2.cpu().numpy())

        probabilities = F.softmax(
            z2,
            dim=1
        )

        print("\nProbabilities:")
        print(probabilities.cpu().numpy())

        confidence, prediction = torch.max(
            probabilities,
            1
        )

    return (
        prediction.item(),
        confidence.item(),
        probabilities.cpu().numpy()
    )

